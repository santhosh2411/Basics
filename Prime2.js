// To specifiy the Prime numbers and non prime numbers
for (let i = 1; i < 10; i++) {
    switch (i) {
        case 1:
            console.log(i, "It is a Prime NUmber or Positive number");
            break;
        case 2:
            console.log(i, "It is a Prime NUmber");
            break;
        case 3:
            console.log(i, "It is a Prime NUmber");
            break;
        case 4:
            console.log(i, "It is not a Prime NUmber because it is divisible by 2");
            break;
        case 9:
            console.log(i, "It is not a Prime NUmber because it is divisible by 3");
            break;
    }
}